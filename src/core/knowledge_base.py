def get_recommendations(problems: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    """Формирует уникальные рекомендации на основе выявленных проблем.

    Returns:
        Список уникальных рекомендаций (по одной на тип проблемы).
    """
    seen: set = set()
    result: List[Dict[str, Any]] = []
    for problem in problems:
        ptype = problem.get('type', '')
        if ptype and ptype not in seen and ptype in _RECOMMENDATIONS:
            seen.add(ptype)
            result.append(_RECOMMENDATIONS[ptype].copy())
    return result
